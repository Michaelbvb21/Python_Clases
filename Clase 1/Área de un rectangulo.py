"""
Cálculo del área de un rectangulo
"""
ancho=float(input('Ingrese el ancho del rectangulo en m: '))
largo=float(input('Ingrese el largo del rectangulo en m: '))

area=ancho*largo

print(f'El area del rectangulo es {area} m2')